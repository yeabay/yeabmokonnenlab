package com.example.demo;

public class Student {
    private int id;
    private String name;
    private String email;
    //setter & getter
    public void setid(int id) {
        this.id = id;
    }
    public int getid() {
        return id;
    }
    public void setname(string name) {
        this.name = name;
    }
    public string getname() {
        return name;
    }
    public void setemail(string email) {
        this.email = email;
    }
    public string getemail() {
        return email;
    }

}
